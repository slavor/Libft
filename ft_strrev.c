#include "libft.h"

char		ft_strrev(char *str)
{
	int		i;
	int		j;
	char	str2;

	i = 0;
	j = -1;
	while (str[i] != '\0')
		i++;
	while (++j < --i)
	{
		str2 = str[j];
		str[j] = str[i];
		str[i] = str2;
	}
	return (str2);
}
